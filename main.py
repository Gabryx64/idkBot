def gen_embed(title, url, description, color):
    return discord.Embed(title = title, url = url, description = description, color = color)

load_dotenv()
client = commands.Bot(command_prefix = os.environ["PREFIX"])

@client.event
async def on_ready():
    print(f"Bot `{client.user}` with id `{client.user.id}` is ready")

@client.event
async def on_message(message):
    if message.content == os.environ["PREFIX"] + "website":
        url = RandomUrl()
        await message.channel.send(embed = gen_embed("Take this website!", "", f"**{ url }**", 0xFF5733))

    if message.content == os.environ["PREFIX"] + "topic":
        await message.channel.send(RandomTopic())

client.run(os.environ["TOKEN"])
